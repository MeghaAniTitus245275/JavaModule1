package oopsday3point;

public class points {
	int x;
	int y;
	public points(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public points()
	{
		
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	

}
